package Beans;

public class Organizacion {
	
	private String strNombre;
	private String strDireccion;
	private String strTelefono;
	private String strIdExt;
	private String strCoEnc;
	
	public String getStrNombre() {
		return strNombre;
	}
	public void setStrNombre(String strNombre) {
		this.strNombre = strNombre;
	}
	public String getStrDireccion() {
		return strDireccion;
	}
	public void setStrDireccion(String strDireccion) {
		this.strDireccion = strDireccion;
	}
	public String getStrTelefono() {
		return strTelefono;
	}
	public void setStrTelefono(String strTelefono) {
		this.strTelefono = strTelefono;
	}
	public String getStrIdExt() {
		return strIdExt;
	}
	public void setStrIdExt(String strIdExt) {
		this.strIdExt = strIdExt;
	}
	public String getStrCoEnc() {
		return strCoEnc;
	}
	public void setStrCoEnc(String strCoEnc) {
		this.strCoEnc = strCoEnc;
	}


}
